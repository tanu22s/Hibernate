package in.co.rays.mapping;

public class bid {
	private int id ;
	private int amount ;
	private int ItemId ;
	
	public bid() {}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}

	public int getItemId() {
		return ItemId;
	}

	public void setItemId(int itemId) {
		ItemId = itemId;
	}
	
	
	
	
}
